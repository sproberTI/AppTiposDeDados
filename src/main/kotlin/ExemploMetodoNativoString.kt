fun main() {
    //lengh() - capitalize() - lowercase() - toUpperCase()
    //isEmpty() - last() - reversed()

    val cidade = "roberto santos da COSTA"
    print("Cidade:              $cidade\n")
    print("Total de caracteres: ${cidade.length}\n")
    print("Captalize:           ${cidade.capitalize()}\n")
    print("Lowercase:           ${cidade.lowercase()}\n")
    print("toUpperCase:         ${cidade.uppercase()}\n")
    print("isEmpty:             ${cidade.isEmpty()}\n")
    print("isBlank:             ${cidade.isBlank()}\n")
    print("last:                ${cidade.last()}\n")
    print("Reversed:            ${cidade.reversed()}\n")

}